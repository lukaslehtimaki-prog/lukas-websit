// A "theme" is a complete visual identity — palette, type pairing, hero layout, card
// treatment, section divider, and services layout. Selection order: explicit editor
// pick (content.themeId) → AI vibe narrows the category pool → deterministic seed.
// Every color is a token so full-dark themes work with the same renderer.

export type HeroStyle = "left" | "center" | "split";
export type CardStyle = "border" | "shadow" | "filled";
