function hashStr(s: string): number {
  let h = 2166136261;
  for (let i = 0; i < s.length; i++) {
    h ^= s.charCodeAt(i);
    h = Math.imul(h, 16777619);
  }
  return h >>> 0;
}

const CATEGORIES: TemplateId[] = ["salon", "trades", "hospitality"];

/**
 * Resolve the theme for a site. Priority: explicit editor pick (themeId) → category
 * pool narrowed by the AI's vibe when it matches → deterministic index from
 * designSeed / business-name hash. Same input → same theme.
 */
export function pickTheme(content: SiteContent, templateId: string): Theme {
  if (content.themeId) {
    const chosen = THEMES.find((t) => t.id === content.themeId);
    if (chosen) return chosen;
  }

  const category = (CATEGORIES.includes(templateId as TemplateId)
    ? templateId
    : "trades") as TemplateId;
  let pool = THEMES.filter((t) => t.categories.includes(category));
  if (!pool.length) pool = THEMES;

  if (content.vibe) {
    const vibed = pool.filter((t) => t.vibes.includes(content.vibe as Vibe));
    if (vibed.length) pool = vibed;
  }

  const seed =
    content.designSeed ??
    hashStr(content.businessName || content.source?.name || "site");
  return pool[seed % pool.length];
}
