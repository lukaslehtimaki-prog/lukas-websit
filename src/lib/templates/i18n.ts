/** Strings for a site language; unknown codes fall back to English. */
export function t(language: string | undefined | null): SiteStrings {
  return STRINGS[(language ?? "fi").toLowerCase()] ?? en;
}

/** Map a business's country (ISO 3166-1 alpha-2) to a supported site language. */
export function languageForCountry(countryCode: string | null | undefined): string {
  const cc = (countryCode ?? "").toUpperCase();
  const map: Record<string, string> = {
    FI: "fi",
    AX: "sv",
    SE: "sv",
    US: "en",
    GB: "en",
    IE: "en",
    AU: "en",
    NZ: "en",
    CA: "en",
    DE: "de",
    AT: "de",
    CH: "de",
    FR: "fr",
    BE: "fr",
    LU: "fr",
    MC: "fr",
    ES: "es",
    MX: "es",
    AR: "es",
    CO: "es",
    CL: "es",
    PE: "es",
    IT: "it",
    PT: "pt",
    BR: "pt",
    NL: "nl",
    CN: "zh",
    TW: "zh",
    HK: "zh",
    SG: "en",
  };
  return map[cc] ?? "en";
}
